package com.kosta.calc.model;

public class Calculator {    //사칙연산 및 데이터 전달
	public int add(int num1, int num2) {
		return num1+num2;
	}
	public int min(int num1, int num2) {
		return num1-num2;
	}
	public int mul(int num1, int num2) {
		return num1*num2;
	}
	public int div(int num1, int num2) {
		return num1/num2;
	}
}
